    public boolean changeDetails()
    {
        boolean result=false;
        try{
        if(lnamebt.isSelected())
        {
           int a=stmt.executeUpdate("update labs set lname='"+lnameIP.getText().toString()+"' where lcode='"+lcodeIP.getText().toString()+"'");
           if(a>0)
               result=true;
           else
               return false;
        }
        
        if(lscodebt.isSelected())
        {
            int b=stmt.executeUpdate("update labs set scode='"+lscodeip.getText().toString()+"' where lcode='"+lcodeIP.getText().toString()+"'");
            if(b>0)
                result=true;
            else
                return false;
        
        }
        
        } catch (SQLException ex) {
            Logger.getLogger(UpdateMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return result;
    }
    
    public boolean changeLabSyllabus()
    {
            boolean result=false;
        try{
  
      Container cn1[]=new Container[] {p1,p2,p3,p4,p5};
      Container cn2[]=new Container[] {ls1,ls2,ls3,ls4,ls5};
      String val[]=new String[10];
      
      
      for(int i=0;i<5;i++)
      {
          
          JRadioButton jr=(JRadioButton) cn1[i];
          JTextArea jt=(JTextArea) cn2[i];
          
          if(jr.isSelected())
          {
              val[i]=jt.getText().toString();
             int a= stmt.executeUpdate("update labs set content='"+val[i]+"' where lcode='"+lcodeIP.getText().toString()+"' and pno='"+(i+1)+"'");
             if(a>0)
                 result=true;
             else
                 return false;
          
          }
          
      }
      
        
        } catch (SQLException ex) {
            Logger.getLogger(UpdateMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        return result;
    }
    
    
    

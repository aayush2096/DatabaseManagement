public void checkcourse()
{
    try{
        rs=stmt.executeQuery("select ctype from subject where scode='"+scodeIP.getText().toString()+"'");
        rs.next();
       String cc=rs.getString(1);
       String uc=courseip.getSelectedItem().toString();
       
       if(cc.equals("U.G") && uc.equals("P.G"))
       {
           if(Integer.parseInt(semip.getSelectedItem().toString())>4)
            {
                JOptionPane.showMessageDialog(null,"Selection of semester above 4th is not allowed in P.G","error", JOptionPane.ERROR_MESSAGE);
                semip.setSelectedIndex(0);
            }
       }
       else if(cc.equals("P.G") && uc.equals("U.G"))
       {
           
       }
       else if(cc.equals("P.G"))
       {
            if(Integer.parseInt(semip.getSelectedItem().toString())>4)
            {
                JOptionPane.showMessageDialog(null,"Selection of semester above 4th is not allowed in P.G","error", JOptionPane.ERROR_MESSAGE);
                semip.setSelectedIndex(0);
            }
       }
           
       
       
       rs.close();
       
    }   catch (SQLException ex) {
            Logger.getLogger(UpdateMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
}
    
    

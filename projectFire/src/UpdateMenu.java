public void callcheck()
{
     if(scodeIP.getText().toString().length()>0){
                
                try{
               
                    rs=stmt.executeQuery("select scheme from `subject` where scode='"+scodeIP.getText()+"'");
                    rs.next();
                    if(rs.getString(1).toString().equals("2015"))
                    {
                        
                        m6.setEnabled(false);
                        m7.setEnabled(false);
                        m8.setEnabled(false);
                    }
                    else if(rs.getString(1).toString().equals("2010"))
                    {
                      
                        m6.setEnabled(true);
                        m7.setEnabled(true);
                        m8.setEnabled(true);
                    }

                    m1.setEnabled(true);
                    m2.setEnabled(true);
                    m4.setEnabled(true);
                    m5.setEnabled(true);
                    m3.setEnabled(true);
                
         
                 } catch (SQLException ex) {
            Logger.getLogger(UpdateMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
            }
            else{
                JOptionPane.showMessageDialog(null,"please enter the subject code first","error",JOptionPane.ERROR_MESSAGE);
             Csyllabus.setSelected(false);
            }
       
}

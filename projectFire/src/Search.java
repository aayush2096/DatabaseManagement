    public boolean calllscode()
    {
        boolean r=false;
        try{
            
            rs=stmt.executeQuery("select * from labs where scode='"+lscodeip.getText().toString()+"' ");
            if(rs.next())
            {
                r=true;
                    l1.setText("Subject code:"+rs.getString(1));
                    l2.setText("Lab code: "+rs.getString(2));
                    l3.setText("Lab name: "+rs.getString(5));
                    l4.setText("Lab credits:"+rs.getString(6));
                do{
                    
                        syllabus[0]=rs.getString(3);
                        syllabus[1]=rs.getString(4);
                        tb.addRow(syllabus);
                    
                }while(rs.next());
                rs.close();
            }
            else
            {
                JOptionPane.showMessageDialog(null,"no records found","error",JOptionPane.ERROR_MESSAGE);
                return false;
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Search.class.getName()).log(Level.SEVERE, null, ex);
        }
        return r;
    }
    

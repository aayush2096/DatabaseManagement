    boolean callbycode(String code)
    {
        tb.setRowCount(0);
       
        
       
        boolean res=false;
       
        try{
            
            rs=stmt.executeQuery("select s.sname,s.scode,s.hours,s.maxmarks from subject s where s.scode='"+code+"'");
            if(rs.next())
            {
                res=true;
             l1.setText("Subject:  "+rs.getString(1).toString());
             
             l2.setText("Code:  "+rs.getString(2).toString());
             l3.setText("Hours:  "+rs.getString(3).toString());
             l5.setText("Max marks:  "+rs.getString(4).toString());
             rs.close();
             rs=stmt.executeQuery("select score from credits where scode='"+code+"'");
         if(rs.next())
         {
                          l4.setText("Credits:  "+rs.getString(1).toString());
                          rs.close();

         }
         else
         {
                          l4.setText("Credits:  ");
                          rs.close();

         }
         rs.close();
             rs=stmt.executeQuery("select module,content from subsyllabus where scode='"+code+"' ");
             
             if(rs.next())
             {
                 res=true;
                 do
                 {
                   syllabus[0]=rs.getString(1);
                   syllabus[1]=rs.getString(2);
                   tb.addRow(syllabus);
             
                 }while(rs.next());
                 rs.close();
             }
             else
             {
                 JOptionPane.showMessageDialog(rootPane,"error while fetching syllabus","Error",JOptionPane.ERROR_MESSAGE);
                 return false;
             }
             
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane,"error while fetching subject","Error",JOptionPane.ERROR_MESSAGE);
                return false;
            }
            
            
        }    catch (SQLException ex) {
                 //Logger.getLogger(Search.class.getName()).log(Level.SEVERE, null, ex);
                 JOptionPane.showMessageDialog(rootPane,ex,"Error",JOptionPane.ERROR_MESSAGE);
             }
        return res;
        
    }
    
